function pintarTabla(lista) {
    let tabla = document.getElementById("tabla");
   tabla.innerHTML="";
    lista.forEach(prenda => {
       let contenido= 
       `<tr>
            <td><img src="${prenda.imagen}" style="width:100px; height:100px;"> </td>
            <td>${prenda.modelo}</td>
            <td>${prenda.tipo}</td>
            <td>${prenda.precio}â‚¬</td>
        </tr>`
    tabla.insertAdjacentHTML("beforeend",contenido) ;
    });
   
}  
function pintarSelect(lista){
   let valor=""; 
   let select=document.getElementById("filtro");
    select.innerHTML="<option value='ejemplo'>Selecciona un tipo</option>"; ;
    lista.forEach(prenda => {
        if(valor!=prenda.tipo){
         let contenido= 
       `<option value="${prenda.tipo}">${prenda.tipo}</option>`
       select.insertAdjacentHTML("beforeend",contenido) ; 
       valor=prenda.tipo;
        }
    });
}
function filtrar(lista){
    let selectValor=document.getElementById("filtro").value;
    if(selectValor=="ejemplo"){
        alert("Debes seleccionar un tipo de prenda");
        pintarTabla(lista);
        return;
    }else{ 
        let listaFiltrada=lista.filter(prenda=>prenda.tipo==selectValor);
        pintarTabla(listaFiltrada);
    }
   
}

